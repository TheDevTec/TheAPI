	XMaterial(int keyId, int id, int data, String... legacy) {
		this.keyId=keyId;
		this.id = id;
		this.data = (byte) data;
		this.legacy = legacy;

		Material mat = null;
		if (!Data.ISFLAT && this.isDuplicated() || (mat = Material.getMaterial(name())) == null)
			for (int i = legacy.length - 1; i >= 0; i--) {
				mat = Material.getMaterial(legacy[i]);
				if (mat != null)
					break;
			}
		material = mat;
	}

